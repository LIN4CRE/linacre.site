def cmd_status():
    """Show project health status."""
    _banner("Project Health")

    # Node version
    nv = _node_version()
    if nv:
        _ok(f"Node.js: {nv}")
    else:
        _warn("Node.js: not found in PATH")

    # npm version
    ok, out, _ = _run([_npm_bin(), "--version"], timeout=5)
    if ok:
        _ok(f"npm: v{out}")
    else:
        _warn("npm: not found")

    # .env status
    if ENV_PATH.exists():
        env_vars = _read_env()
        found_keys = [k for k in KEYS_TO_SYNC if k in env_vars and env_vars[k]]
        _ok(f".env: {len(found_keys)}/{len(KEYS_TO_SYNC)} API keys configured")
        for k in KEYS_TO_SYNC:
            if k in env_vars and env_vars[k]:
                masked = env_vars[k][:8] + "..." if len(env_vars[k]) > 8 else "***"
                _ok(f"  {k} = {masked}")
            else:
                _warn(f"  {k} = (missing)")
    else:
        _warn(".env: not found — run: python linacre.py setup")

    # Vercel
    if _is_vercel_logged_in():
        _ok("Vercel CLI: authenticated")
    else:
        _warn("Vercel CLI: not logged in")

    # node_modules
    nm = PROJECT_DIR / "node_modules"
    if nm.exists():
        _ok("node_modules: installed")
    else:
        _warn("node_modules: missing — run: npm install")

    # Git
    ok, out, _ = _run(["git", "rev-parse", "--short", "HEAD"], timeout=5, cwd=str(PROJECT_DIR))
    if ok:
        _ok(f"Git HEAD: {out}")
    else:
        _warn("Git: not a repository or git not found")

    print()


