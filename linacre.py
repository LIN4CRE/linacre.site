# ─── Utilities ───────────────────────────────────────────────────────────────

def _set_encoding():
    """Force UTF-8 output on Windows."""
    if hasattr(sys.stdout, "reconfigure"):
        try:
            sys.stdout.reconfigure(encoding="utf-8")
        except Exception:
            pass


def _banner(text: str):
    print(f"\n{AMBER}{BOLD}{'─' * 52}{RESET}")
    print(f"{AMBER}{BOLD}  {text}{RESET}")
    print(f"{AMBER}{BOLD}{'─' * 52}{RESET}\n")


def _ok(msg: str):
    print(f"  {GREEN}✓{RESET} {msg}")


def _warn(msg: str):
    print(f"  {RED}⚠{RESET} {msg}")


def _info(msg: str):
    print(f"  {DIM}→{RESET} {msg}")


def _get_windows_env(name: str) -> str | None:
    """Read a variable from Windows Registry, falling back to process env."""
    if sys.platform != "win32":
        return os.environ.get(name)
    import winreg
    # User env
    try:
        with winreg.OpenKey(winreg.HKEY_CURRENT_USER, "Environment") as key:
            value, _ = winreg.QueryValueEx(key, name)
            return value
    except FileNotFoundError:
        pass
    # System env
    try:
        with winreg.OpenKey(
            winreg.HKEY_LOCAL_MACHINE,
            r"SYSTEM\CurrentControlSet\Control\Session Manager\Environment",
        ) as key:
            value, _ = winreg.QueryValueEx(key, name)
            return value
    except FileNotFoundError:
        pass
    return os.environ.get(name)


def _read_env() -> dict[str, str]:
    """Parse the existing .env file into a dict."""
    env_vars: dict[str, str] = {}
    if ENV_PATH.exists():
        for line in ENV_PATH.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if line and not line.startswith("#") and "=" in line:
                k, v = line.split("=", 1)
                env_vars[k.strip()] = v.strip()
    return env_vars


def _write_env(env_vars: dict[str, str]):
    """Write dict back to .env with header comment."""
    lines = [
        "# Autogenerated by linacre.py",
        "# Do not commit this file to git\n",
    ]
    for k, v in env_vars.items():
        lines.append(f"{k}={v}")
    ENV_PATH.write_text("\n".join(lines) + "\n", encoding="utf-8")


def _run(cmd_args: list[str], input_data: str | None = None, timeout: int | None = None, cwd: str | None = None):
    """Run a subprocess and return (ok, stdout, stderr)."""
    try:
        stdin_val = None if input_data is not None else subprocess.DEVNULL
        res = subprocess.run(
            cmd_args,
            text=True,
            input=input_data,
            stdin=stdin_val,
            capture_output=True,
            timeout=timeout,
            cwd=cwd,
        )
        return res.returncode == 0, res.stdout.strip(), res.stderr.strip()
    except subprocess.TimeoutExpired:
        return False, "", "Command timed out"
    except Exception as e:
        return False, "", str(e)


def _is_vercel_logged_in() -> bool:
    """Check Vercel CLI auth config files."""
    paths = [
        Path(os.environ.get("LOCALAPPDATA", "")) / "com.vercel.cli" / "auth.json",
        Path(os.environ.get("APPDATA", "")) / "com.vercel.cli" / "auth.json",
        Path(os.environ.get("APPDATA", "")) / "xdg.data" / "com.vercel.cli" / "auth.json",
        Path.home() / ".config" / "vercel" / "auth.json",
    ]
    for p in paths:
        if p.exists():
            try:
                data = json.loads(p.read_text(encoding="utf-8"))
                if data.get("token"):
                    return True
            except Exception:
                pass
    return False


def _node_version() -> str | None:
    """Get installed Node.js version."""
    ok, out, _ = _run(["node", "--version"], timeout=5)
    return out if ok else None


def _npm_bin() -> str:
    """Return the npm command name (npm.cmd on Windows)."""
    return "npm.cmd" if sys.platform == "win32" else "npm"


