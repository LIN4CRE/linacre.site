def cmd_setup():
    """Scan Windows Registry for developer API keys and sync to .env."""
    _banner("Environment Setup")
    _info("Scanning Windows Registry for developer API keys...")

    env_vars = _read_env()
    updated = False

    for key in KEYS_TO_SYNC:
        val = _get_windows_env(key)
        if val:
            if env_vars.get(key) != val:
                env_vars[key] = val
                _ok(f"Synchronized: {key}")
                updated = True
            else:
                _ok(f"Up to date: {key}")
        else:
            _warn(f"Not found in Windows environment: {key}")

    # Ensure PORT is set
    if "PORT" not in env_vars:
        env_vars["PORT"] = "3000"

    _write_env(env_vars)

    if updated:
        _ok(f"Workspace .env written → {ENV_PATH}")
    else:
        _info("No changes needed — .env is already current.")


def cmd_sync():
    """Push local .env secrets to Vercel environment."""
    _banner("Vercel Secret Sync")

    vercel_bin = "vercel.cmd" if sys.platform == "win32" else "vercel"

    if not _is_vercel_logged_in():
        _warn("Vercel CLI is not logged in.")
        _info("Run: vercel login")
        return

    ok, stdout, stderr = _run([vercel_bin, "whoami"], timeout=5, cwd=str(PROJECT_DIR))
    if not ok:
        _warn(f"Vercel session validation failed: {stderr}")
        _info("Run: vercel login")
        return
    _ok(f"Logged in as: {stdout}")

    if not ENV_PATH.exists():
        _warn("No .env file found. Run: python linacre.py setup")
        return

    env_vars = _read_env()
    secrets = {k: v for k, v in env_vars.items() if k in KEYS_TO_SYNC and v}

    if not secrets:
        _info("No developer API keys found in .env to sync.")
        return

    _info(f"Found {len(secrets)} secrets to push to Vercel.")

    for k, v in secrets.items():
        _info(f"Syncing {k}...")
        for env_name in ("production", "preview", "development"):
            res = subprocess.run(
                [vercel_bin, "env", "add", k, env_name, "--value", v, "--yes"],
                capture_output=True, text=True,
                cwd=str(PROJECT_DIR),
            )
            out = (res.stderr or res.stdout or "").strip()
            if res.returncode == 0:
                _ok(f"  {env_name} ✓")
            elif "already been added" in out:
                _ok(f"  {env_name} ✓ (already exists)")
            else:
                _warn(f"  {env_name} ✗: {out[:60]}")

    _ok("Vercel environment sync complete!")


def cmd_dev():
    """Install deps if needed, sync env, start dev server."""
    _banner("Dev Server")

    # Auto-install if node_modules is missing
    nm = PROJECT_DIR / "node_modules"
    if not nm.exists():
        _info("Installing npm dependencies (first run)...")
        subprocess.run([_npm_bin(), "install"], cwd=str(PROJECT_DIR))
        print()

    # Sync env
    cmd_setup()
    print()

    # Start dev server (tsx api/server.ts)
    _info("Starting dev server...")
    try:
        tsx_bin = "tsx.cmd" if sys.platform == "win32" else "tsx"
        # Try local tsx first
        local_tsx = PROJECT_DIR / "node_modules" / ".bin" / ("tsx.cmd" if sys.platform == "win32" else "tsx")
        if local_tsx.exists():
            tsx_bin = str(local_tsx)

        subprocess.run(
            [tsx_bin, "api/server.ts"],
            cwd=str(PROJECT_DIR),
        )
    except KeyboardInterrupt:
        print(f"\n{DIM}  Server stopped.{RESET}")


